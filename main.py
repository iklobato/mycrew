"""A sample Module"""


def add(a, b):
    """
    This function adds two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The sum of a and b.
    """
    return a + b


def subtract(a, b):
    """
    This function subtracts two numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The difference of a and b.
    """
    return a - b
